for (const lang of Object.keys(newTranslations)) {
  const match = content.match(new RegExp(`(${lang}:\\s*\\{[^}]+\\})`));
  if (match) {
    let langBlock = match[1];
    for (const [key, val] of Object.entries(newTranslations[lang])) {
      if (!langBlock.includes(key + ":")) {
        // Insert before the last closing brace of this language block
        langBlock = langBlock.replace(/\\s*\\}$/, `,\\n    ${key}: "${val}"\\n  }`);
      }
    }
    content = content.replace(match[1], langBlock);
  }
}

